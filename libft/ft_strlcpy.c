#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t	ssize;

	if (!dst || !src)
		return (0);
	ssize = ft_strlen(src);
	if (ssize < dstsize)
		ft_memcpy(dst, src, ssize + 1);
	else if (dstsize)
	{
		ft_memcpy(dst, src, dstsize - 1);
		dst[dstsize - 1] = '\0';
	}
	return (ssize);
}
